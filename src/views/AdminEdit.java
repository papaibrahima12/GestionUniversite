    public void initAdmin() {
        Connection conn = BD.GetConnection();
        try {
            Statement stm = conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = stm.executeQuery("SELECT * FROM users WHERE id = \"" + userV + "\"");
            rs.first();
            titre.setText("Modification de l'Utilisateur " + rs.getString("prenom") + " " + rs.getString("nom"));
            nom.setText(rs.getString("nom"));
            prenom.setText(rs.getString("prenom"));
            login.setText(rs.getString("login"));
            herPass = rs.getString("motDePass");
           
        } catch (SQLException ex) {
            Logger.getLogger(AdminEdit.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

